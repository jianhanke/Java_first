public class Example15{
	public static void main(String[] args){
		System.out.println("计算绝对值的结果:"+Math.abs(-20));
		System.out.println("大于参数的最小整数:"+Math.ceil(-5.6));
		System.out.println("求小于参数的最大整数:"+Math.floor(4.2));
		System.out.println("四舍五入:"+Math.round(8.47879));
		System.out.println("两个数的较大值:"+Math.max(2.1,-2.1));
		System.out.println("两个数的较小值:"+Math.min(2.1,-2.1));
		System.out.println("大于等于0.0小于1.0随机数:"+Math.random());
		System.out.println(Math.random());
		System.out.println(Math.random());
	}
}